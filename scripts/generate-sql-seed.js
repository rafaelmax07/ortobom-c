const fs = require('fs');
const path = require('path');
const slugify = require('slugify');

const dataPath = path.join(__dirname, 'seed-products.json');
const outputPath = path.join(__dirname, '../supabase/seed.sql');

if (!fs.existsSync(dataPath)) {
  console.error('❌ seed-products.json not found. Run scrape.js first.');
  process.exit(1);
}

const raw = JSON.parse(fs.readFileSync(dataPath, 'utf-8'));

// Support both old format (array) and new format ({ products, banners })
const products = Array.isArray(raw) ? raw : raw.products || [];
const banners = Array.isArray(raw) ? [] : raw.banners || [];

// ────────────────────────────────────────────
// Helpers
// ────────────────────────────────────────────
const escape = (str) => (str ? `'${String(str).replace(/'/g, "''")}'` : 'NULL');
const escapeHtml = (str) => (str ? `'${String(str).replace(/'/g, "''").replace(/\n/g, '\\n')}'` : 'NULL');

function makeSlug(name) {
  return slugify(name, { lower: true, strict: true, locale: 'pt' });
}

// Standard size order for mattresses/bases
const SIZE_ORDER = ['Solteiro', 'Casal', 'Queen', 'King', 'Super King', 'Padrão'];

function sortVariants(variants) {
  return [...variants].sort((a, b) => {
    const ai = SIZE_ORDER.indexOf(a.size);
    const bi = SIZE_ORDER.indexOf(b.size);
    return (ai === -1 ? 99 : ai) - (bi === -1 ? 99 : bi);
  });
}

// ────────────────────────────────────────────
// Build SQL
// ────────────────────────────────────────────
let sql = '-- Seed Data (Generated by generate-sql-seed.js)\n';
sql += `-- Generated at: ${new Date().toISOString()}\n`;
sql += `-- Products: ${products.length}, Banners: ${banners.length}\n\n`;

// ── Categories ────────────────────────────────────────────────────────────────
const categories = [...new Set(products.map((p) => p.category_slug))];
sql += '-- ═══ Categories ═══════════════════════════════════\n';
categories.forEach((slug) => {
  const name = slug.charAt(0).toUpperCase() + slug.slice(1).replace(/-/g, ' ');
  sql += `INSERT INTO categories (name, slug) VALUES ('${name}', '${slug}') ON CONFLICT (slug) DO NOTHING;\n`;
});
sql += '\n';

// ── Banners ───────────────────────────────────────────────────────────────────
const finalBanners = banners.length > 0 ? banners : [
  {
    title: 'Saldão dos Sonhos Ortobom - Até 50% OFF',
    image_desktop_url: 'https://images.unsplash.com/photo-1505693416388-ac5ce068fe85?q=80&w=1920&auto=format&fit=crop',
    image_mobile_url: 'https://images.unsplash.com/photo-1505693416388-ac5ce068fe85?q=80&w=600&auto=format&fit=crop',
    link: '/c/colchoes'
  },
  {
    title: 'Colchões de Alta Tecnologia com Frete Grátis',
    image_desktop_url: 'https://images.unsplash.com/photo-1631049307264-da0ec9d70304?q=80&w=1920&auto=format&fit=crop',
    image_mobile_url: 'https://images.unsplash.com/photo-1631049307264-da0ec9d70304?q=80&w=600&auto=format&fit=crop',
    link: '/c/colchoes'
  },
  {
    title: 'Bases Sommier e Camas Baú - Conforto Exclusivo',
    image_desktop_url: 'https://images.unsplash.com/photo-1540518614846-7eded433c457?q=80&w=1920&auto=format&fit=crop',
    image_mobile_url: 'https://images.unsplash.com/photo-1540518614846-7eded433c457?q=80&w=600&auto=format&fit=crop',
    link: '/c/camas'
  }
];

sql += '-- ═══ Banners ══════════════════════════════════════\n';
sql += 'TRUNCATE TABLE banners RESTART IDENTITY CASCADE;\n';
finalBanners.forEach((b, i) => {
  sql += `INSERT INTO banners (title, image_desktop_url, image_mobile_url, link, position, is_active)\n`;
  sql += `  VALUES (${escape(b.title)}, ${escape(b.image_desktop_url)}, ${escape(b.image_mobile_url || b.image_desktop_url)}, ${escape(b.link || '#')}, ${i}, true);\n`;
});
sql += '\n';

// ── Products & Variants & Images ──────────────────────────────────────────────
sql += '-- ═══ Products, Variants & Images ═══════════════════\n';
sql += 'TRUNCATE TABLE product_images CASCADE;\n';
sql += 'TRUNCATE TABLE variants CASCADE;\n\n';

sql += 'DO $$\n';
sql += 'DECLARE\n';
sql += '  cat_id uuid;\n';
sql += '  prod_id uuid;\n';
sql += 'BEGIN\n';

products.forEach((p) => {
  const slug = p.slug || makeSlug(p.name);

  sql += `\n  -- ── Product: ${p.name} ──────────────────────\n`;
  sql += `  SELECT id INTO cat_id FROM categories WHERE slug = '${p.category_slug}';\n`;

  sql += `  INSERT INTO products (name, slug, description, description_html, featured_image, category_id, is_active)\n`;
  sql += `    VALUES (${escape(p.name)}, '${slug}', ${escape(p.description)}, ${escapeHtml(p.description_html)}, ${escape(p.featured_image)}, cat_id, true)\n`;
  sql += `    ON CONFLICT (slug) DO UPDATE SET\n`;
  sql += `      featured_image = EXCLUDED.featured_image,\n`;
  sql += `      description = EXCLUDED.description,\n`;
  sql += `      description_html = EXCLUDED.description_html\n`;
  sql += `    RETURNING id INTO prod_id;\n`;

  sql += `  IF prod_id IS NULL THEN\n`;
  sql += `    SELECT id INTO prod_id FROM products WHERE slug = '${slug}';\n`;
  sql += `  END IF;\n`;

  // Images (product_images table)
  const images = p.images || (p.featured_image ? [p.featured_image] : []);
  images.forEach((imgUrl, pos) => {
    sql += `  INSERT INTO product_images (product_id, url, alt, position)\n`;
    sql += `    VALUES (prod_id, ${escape(imgUrl)}, ${escape(p.name)}, ${pos});\n`;
  });

  // Variants
  const variants = p.variants || [];
  const sorted = sortVariants(variants);

  sorted.forEach((v, idx) => {
    // Skip "sob medida"
    if (v.size.toLowerCase().includes('sob medida') || v.size.toLowerCase().includes('medida')) return;

    const price = Math.round(v.price || 0);
    // If no compare_at_price scraped, use the product-level base or add 30%
    const compareBase = p.compare_at_price_base || 0;
    const comparePrice = compareBase > 0 && idx === 0
      ? Math.round(compareBase)
      : Math.round(price * 1.3);

    const variantSku = `${slug}-${v.size.replace(/\s+/g, '').toUpperCase().substring(0, 5)}`;

    sql += `  INSERT INTO variants (product_id, size, price, compare_at_price, sku, dimensions, stock)\n`;
    sql += `    VALUES (prod_id, ${escape(v.size)}, ${price}, ${comparePrice}, '${variantSku}', ${escape(v.dimensions || null)}, 50);\n`;
  });
});

sql += '\nEND $$;\n';

fs.writeFileSync(outputPath, sql);
console.log(`✅ SQL Seed generated:`);
console.log(`   File: ${outputPath}`);
console.log(`   Products: ${products.length}`);
console.log(`   Banners: ${banners.length}`);
console.log(`   Categories: ${categories.join(', ')}`);
